"""
* Escribe un programa que reciba dos números y 
* muestre la división y el residuo de la división.
"""

#Mensaje
print("\n\n\t\t**** CALCULADORA DE DIVISIONES ****\n")

#Lectura de datos
valorA = float(input("\t\tINGRESA EL VALOR DE A: "))
valorB = float(input("\t\tINGRESA EL VALOR DE B: "))

#Se realizan las operaciones
print(f"\n\t\t{valorA} / {valorB} = {valorA / valorB}")
print(f"\t\t{valorA} % {valorB} = {valorA % valorB}\n")

