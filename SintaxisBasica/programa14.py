"""
* Escribe un programa que reciba dos números y 
* muestre el resultado de sumarlos y multiplicarlos.
"""

#Mensaje
print("\n\n\t\t**** CALCULADORA BÁSICA ****\n")

#Lectura de datos
valorA = float(input("\t\tINGRESA EL VALOR A: "))
valorB = float(input("\t\tINGRESA EL VALOR B: "))

#Se muestran las operaciones
print(f"\n\t\t{valorA} + {valorB} = {valorA + valorB}")
print(f"\t\t{valorA} - {valorB} = {valorA - valorB}")
print(f"\t\t{valorA} * {valorB} = {valorA * valorB}")
print(f"\t\t{valorA} / {valorB} = {valorA / valorB}\n")
